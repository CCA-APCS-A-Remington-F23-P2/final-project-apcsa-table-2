public class Dog{
    private int xPos;
    private int yPos;
    private int width;
    private int height;
    private int speed;
    private int jumpHeight;

    public Dog(int x, int y, int w, int h, int spd, int jH){
        xPos = x;
        yPos = y;
        width = w;
        height = h;
        speed = spd;
        jumpHeight = jH;
    }
}